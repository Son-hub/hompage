package dto;

public class MovieDto {
	private int num;
	private String je;
	private String gam;
	private String bae;
	private int score;
	private String hugi;
	private String img;
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getJe() {
		return je;
	}
	public void setJe(String je) {
		this.je = je;
	}
	public String getGam() {
		return gam;
	}
	public void setGam(String gam) {
		this.gam = gam;
	}
	public String getBae() {
		return bae;
	}
	public void setBae(String bae) {
		this.bae = bae;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getHugi() {
		return hugi;
	}
	public void setHugi(String hugi) {
		this.hugi = hugi;
	}
}
